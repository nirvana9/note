#include<iostream>
using namespace std;


int main(int argc ,char ** argv)
{
	int a;
	short b;
	char c;
	long d;
	double e;

	cout << "sizeof int = " << sizeof(a) <<endl;
	cout << "sizeof short = " << sizeof(b) <<endl;
	cout << "sizeof char = " << sizeof(c) <<endl;
	cout << "sizeof long = " << sizeof(d) <<endl;
	cout << "sizeof double = " << sizeof(e) <<endl;

	return 0;
}

